#conversão de tipos, coerção
#type convertion, typecasting, coercion é o ato de converter um tipo em outro
#tipos imutáveis e primitivos:
# str, int, float, bool
print('1' + 1)
print('a' + 'b') #esse tipo de coisa é chamada de polimorfismo, 
#o mesmo operador é usado para coisas diferente

print(int('1'), type('1'))
print(int('1') + 1)
print(type(float('1') + 1))
print(bool(' '))
print(str(11) + 'b')
